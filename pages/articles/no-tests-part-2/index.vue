        <p>
          Поэтому, если вы приобретёте тест в такой ситуации, ничего страшного не произойдёт. Он не заставит вас сделать
          что-то критически неправильное. Главное, чтобы не случилось такого, когда бурые водоросли в данный момент
          присутствуют закономерно, а вы, купив тест, получите завышенные значения из-за его низкого качества или брака,
          и на этом основании перезапустите аквариум без необходимости.
        </p>

        <h2 class="title font-weight-regular text-h4 my-10">
          Ph - тест на кислотность воды
        </h2>

        <p>
          Тест на pH - практически бесполезный тест. Знание этого параметра вам ничего не дает. Вообще pH - это
          кислотность воды. Растения любят pH ниже 7 - слабокислую воду. Многие измеряют pH водопроводной воды и
          говорят, что pH у воды такой-то. Видят высокий pH и по совету неопытных людей из интернета начинают добавлять
          разные кислоты, чтобы понизить pH. Особенно смешно иногда получается с ортофосфорной кислотой. В попытках
          уменьшить pH аквариумист постепенно повышает фосфат в аквариуме до очень высоких значений, при этом pH в
          аквариуме через небольшое время будет возвращаться к исходному значению. А от фосфата вам придется
          избавляться не один месяц, т.к. он очень сильно впитывается в грунт.
        </p>

        <p>
          У воды нет самостоятельного показателя pH. Стоит вам немного добавить углекислого газа в воду, и вот pH уже
          снижается. pH - это результативный признак. Растения будут расти не от низкого pH, а от наличия CO2. А если
          есть газ, то и pH будет низким. Чувствуете логику? Где причина, а где следствие?
        </p>

        <p>
          Если Kh адекватный (от 0 до 6), то при подаче газа pH в любом случае будет ниже 7. Если у вас Kh высокий
          и вы подаете газ,
          то pH тоже будет высоким. Если Kh нормальный, то при правильной подаче газа pH в любом случае
          будет в диапазоне от 5.5 до 7. Какой смысл его измерять? Нет оптимальных значений, при которых растения будут
          расти лучше, или удобрений, которые при pH 6.5 будут усваиваться лучше. Современные фирменные удобрения
          выдерживают большой диапазон pH благодаря сильным хелаторам.
        </p>

        <p>
          На своем опыте я не видел корреляции между pH и ростом растений. Тут скорее прямая корреляция между
          концентрацией CO2 и ростом растений.
        </p>

        <p>
          Некоторые авторы предупреждают, что при изменении pH на величину больше 0.5 в течение дня могут погибнуть
          гидробионты. Но на самом деле здесь нужно смотреть на вопрос шире.
        </p>

        <p>
          Большинство рыбок, которые принято использовать в аквариумах-травниках с CO2, хорошо переносят изменения pH за
          2-3 часа (время от включения газа до установления максимального значения) на 1-1.5 единицы. Такие изменения
          встречаются даже в природе. Разнообразные тетры, например неоны или аманды, хорошо живут в кислой воде. Но
          моллинезий или гуппи лучше в таких аквариумах не содержать. Они предпочитают нейтральную или слабощелочную
          воду со средними параметрами жесткости. Поэтому при покупке живности лучше узнать об условиях содержания тех
          или иных видов.
        </p>

        <p>
          Раз уж зашла речь о живности в аквариуме-травнике, то я бы хотел дать один совет: не увлекайтесь покупкой
          дорогой живности в большом количестве. Вы будете совершать ошибки по неопытности, примите это как факт.
          Когда-нибудь вы перельете удобрения, добавите избыточное количество сайдекса или перекиси, перегреете аквариум
          или перегазуете, будете делать очень большие и частые подмены, очень часто пересаживать растения, давать очень
          мощный свет и тем самым создавать избыточный стресс для гидробионтов. Они могут даже выпрыгивать из аквариума.
          Часть или вся живность может погибнуть. И переживать лишний раз или менять условия в аквариуме из-за живности
          может стать очень неприятным моментом.
        </p>

        <p>
          У вас и так будет целый ворох проблем, связанных с выращиванием растений. Лучше сосредоточиться сначала на
          этом. А уже когда станет лучше получаться поддерживать стабильные параметры в аквариуме и меньше стрессовать
          живность, тогда можно будет покупать более дорогие и редкие виды. А для начала бесплатных катушек, дешевых
          неокардин или рыбок от местных разводчиков будет вполне достаточно для помощи в чистке аквариума.
        </p>

        <p>
          Очень хорошо о pH написал
          <a
            target="_blank"
            href="https://www.2hraquarist.com/blogs/hot-topics"
          >Денис Вонг.</a>
          Единственная польза, которую можно извлечь из теста на pH - это
          настройка газа. Но тут можно обойтись и без этого, просто настраивая газ по поведению гидробионтов, как
          это рекомендуют в своих статьях компания ADA.
        </p>

        <p>
          Какой потенциальный вред может быть от теста на pH? Допустим, измерение pH показало завышенный уровень. Вы
          будете думать, что pH слишком высок для выращивания растений, и в таком случае неопытный аквариумист может
          последовать совету налить кислоты в аквариум, добавить чрезмерно большое количество газа или предпринять
          какое-либо другое действие, дестабилизирующее аквариум.
        </p>

        <p>
          Еще один пример, только уже для увеличения pH. Есть мнение, что удобрения (в основном это касается
          микроудобрений и железа) усваиваются лучше при pH около 6.7. И вот вы делаете измерение, и pH-тест показал
          значение 6.3. Какие ваши действия? В голову приходит вариант уменьшить количество газа и тем самым повысить
          pH. Но в таком случае, если при pH 6.3 живность себя чувствовала хорошо, и вы уменьшаете подачу газа, то
          потенциально в погоне за усвоением удобрений вы намеренно уменьшаете концентрацию CO2 в воде, который является
          элементом номер один для роста растений. Получается, что вы идете в обратном направлении, когда в то же время
          растительная аквариумистика стремится к тому, чтобы концентрация была как можно больше.
        </p>

        <p>
          Хорошо, вы скажете: "Ну тогда нужно повысить Kh! И тем самым при одной и той же подаче и концентрации газа в
          воде pH повысится" (пренебрежем гидрокарбонатным равновесием, так как оно не будет значительно смещаться в
          данном диапазоне pH). Да, так и есть. Но если у вас Kh равен 0 для выращивания Тонин, или вы выращиваете
          Роталу Валлихи с максимальным Kh 3? Как быть в этом случае?
        </p>

        <p>
          Или вот еще задача: что если у вас грунт типа soil, и он даже без газа подкисляет воду до pH 6.5? А при подаче
          газа pH стремится вообще к 6 и ниже. Как быть в таком случае?
          При этом аквариумы на soil выглядят зачастую значительно лучше, и выращивать растения в них проще.
        </p>

        <p>
          Ответ простой: не нужно ничего выравнивать. Ваши удобрения выдержат любой разумный диапазон pH, и такими
          корректировками вы сделаете только хуже. В конечном итоге успех будет зависеть не от этого, а от более важных
          составляющих в аквариуме.
        </p>

        <p>
          Вполне возможно, что лимитирование по газу будет более выгодно для вашего аквариума, и достижение максимальной
          концентрации не лучший вариант. Такая ситуация возможна, когда химия воды не очень хорошая, чрезмерно много
          света или по каким-либо причинам питание не может усваиваться в большей степени, чем сейчас. Но именно газ не
          давал растениям расти быстрее, и питания в такой ситуации хватало. Давая больше газа, вы ослабляете
          ограничивающий фактор, но не устраняете проблему с питанием, и в такой ситуации растениям может стать хуже. Я
          наблюдал такое в своих аквариумах из-за плохого грунта или дисбаланса по удобрениям. Это может стать хорошим
          объяснением причины ухудшения растений при увеличении CO2, но также и поводом задуматься: а всё ли правильно
          настроено в аквариуме?
        </p>

        <h2 class="title font-weight-regular text-h4 my-10">
          Дропчекер
        </h2>

        <p>
          Дропчекер — это небольшой прозрачный похожий на каплю резервуар с синей или зеленой жидкостью.
          Реже с желтой. Он ближайший родственник pH-теста и нужен для определения концентрации CO2 в воде.
        </p>

        <p>
          Сложно представить, какой вред он может нанести. Но есть нюансы. Так же как и в случае с pH-тестом, он может
          создавать неправильное представление о количестве углекислого газа в воде.
        </p>

        <p>
          От производителя к производителю жидкость и форма самого устройства сильно отличаются. И индикация уровня CO2
          может существенно варьироваться.
        </p>

        <p>
          Показания сильно зависят от расположения дропчекера в аквариуме. И при хорошем распылении газа такого
          оптимального места может не быть вовсе. Мелкая пыль CO2 попадает в дропчекер и делает его более желтым,
          вследствие чего вы будете уменьшать количество газа и отдалять аквариум от оптимального состояния.
        </p>

        <p>
          Жидкость нужно менять на регулярной основе. Начинающий аквариумист может даже про это не знать.
        </p>

        <p>
          Дропчекер очень инертный. Он покажет значения равновесной концентрации газа только через 3-4 часа после того,
          как CO2 уже будет в пике. А пик, как мы помним, наступает через 2-3 часа после начала подачи газа.
          Это может создавать ложное впечатление, что газ накапливается в течение дня и к
          вечеру будет в критической точке, из-за чего гидробионты могут погибнуть. Нет, это не так. Если вы выставили
          хорошую подачу газа и создали сильное волнение на поверхности, то даже круглосуточная подача CO2 не навредит
          живности (в моем 35-литровом аквариуме у меня несколько раз ЭМК не отключал газ на ночь, и при 2 пузырьках в
          секунду никто в аквариуме не погиб). Это происходит потому, что растения не потребляют даже одного процента от
          общего количества газа. Почти весь CO2 просто выветривается. Таким образом, концентрация газа при свете и без
          света не будет отличаться практически ничем.
        </p>

        <p>
          Еще одно заблуждение заключается в том, что дропчекер защитит вашу живность от чрезмерного количества CO2. Но
          на самом деле, пока он отреагирует на повышение газа, все гидробионты уже отправятся к Нептуну. Я сам с этим
          сталкивался. Когда я заметил всю живность на поверхности воды, дропчекер был все еще зеленым.
        </p>

        <p>
          Даже у ADA рекомендация по подаче газа заключается в том, что нужно начинать с 1 пузырька в секунду на
          60-литровый аквариум и наблюдать за ростом растений, а не спрашивать дропчекер, который висит в углу
          аквариума. При любых манипуляциях с газом нужно находиться около аквариума и иметь возможность исправить
          ситуацию. К сожалению, бывает, что газ бесконтрольно повышается из-за неисправности оборудования, и обычно вас
          не будет рядом в этот момент.
        </p>

