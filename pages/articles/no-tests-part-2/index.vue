        <h2 class="title font-weight-regular text-h4 my-10">
          NO3 и PO4 - тесты на нитрат и фосфат
        </h2>

        <p>
          И вот мы добрались до самого корня зла 🙂
        </p>

        <p>
          Я буду рассматривать оба элемента, так как они чаще всего идут в паре в удобрениях, в обсуждениях и т.д.
        </p>

        <p>
          Что же опасного может быть в таких простых элементах? На самом деле ничего супер страшного нет, а вот в
          выводах аквариумиста относительно измерений тестами — есть.
        </p>

        <p>
          Опишу, как чаще всего используют тесты. Чаще всего аквариумисты считают нужным первым делом понять, сколько же
          в день потребляется нитрата и фосфата в их растительном аквариуме. И вот аквариумист берет тест на нитрат,
          делает измерение утром и делает измерение вечером, пытаясь потом посчитать дневное потребление. У этого
          действия есть несколько потенциальных проблем.
        </p>

        <p>
          Во-первых, точность тестов. Ни один тест на нитрат в широком потреблении не измерит точно вам разницу.
          Погрешность теста в лучшем случае 2-3 мг/л, а то и 5. На некоторых шкалах отметки находятся на значениях 0,
          10, 25. Как вам такая точность?
        </p>

        <p>
          Во-вторых, погрешность теста это не самое страшное. А самое страшное то, что тесты зачастую просто врут или
          вообще не работают.
        </p>

        <p>
          В-третьих, потребление аквариума может меняться и оно будет меняться. Тут есть зависимость от многих настроек
          в аквариуме или просто стрижек. Придется пересчитывать потребление, даже если вы его уже нашли и у вас
          получилось сделать это довольно точно.
        </p>

        <p>
          И вот представьте, что вы измерили утром 10 нитрата, а вечером у вас на шкале 5. Сколько вы добавите нитрата
          на следующий день? Наверное, попробуете добавить 5 нитрата. Добавляете удобрение до 10, измеряете вечером. А
          нитрата уже не 5, как ожидалось, а 10. "Ну значит не буду добавлять, так как аквариум не потребил ничего", —
          думаете вы. И вы то добавляете удобрения, то не добавляете. А ведь аквариуму нужна стабильность, любой опытный
          аквариумист вам это подтвердит.
        </p>

        <p>
          У моего знакомого тест показывал нитрат в водопроводе больше 20. Нетрудно догадаться, какой вывод он сделал —
          не нужно добавлять нитрат. И он добавлял один только фосфат. А росли только водоросли, а не растения. Но
          почему-то когда он начинал добавлять нитрат, то растения оживали. Интересный момент, не правда ли?
        </p>

        <p>
          Мало этого, тесты могут не работать совсем! И вы по своей неопытности будете верить показанию. Потому что у
          вас и мысли на данном этапе не может возникнуть, что тесты, которые сделали умные люди, могут не работать.
          Сколько аквариумистов в надежде увидеть заветную единицу фосфата видели прозрачную жидкость вместо голубой и
          начинали вливать фосфат как сумасшедшие, виня производителя удобрений в некачественности или думая, что
          аквариум потребляет столько удобрений!
        </p>

        <p>
          Узнали себя? Вы думаете: "Нужно же добавить удобрений!" Добавляете. Опять 0. "Нужно еще!"
          Добавляете еще больше! И так до тех пор, пока не появятся водоросли. Я сам на это попадался, с очень хорошим
          немецким тестом. А что говорить о дешевых!
        </p>

        <p>
          Живность, конечно, вряд ли вы отравите нитратом, но здоровья им это не прибавит.
          А бывает и наоборот. Нитрата нет, а тест показывает 10-15 мг/л. "Ну значит добавлять не нужно". А в это время
          растения начнут голодать и опять приходят водоросли.
        </p>

        <p>
          Тест на нитрат способен на многое. Вы можете его проверить на поверочном растворе. А следующее измерение через
          пару дней будет уже неправильным, потому что он просто испортился. Или может запросто показать в свежем осмосе
          10 нитрата, а в отстоявшемся том же самом осмосе 0. Чудеса.
        </p>

        <p>
          А еще становится интереснее, когда у аквариумиста две разные бутылки с нитратом и фосфатом, и он делает тесты
          на нитрат и фосфат в аквариуме. Ох уж тут начинается! Один тест показал одно, другой другое. Начинаем
          регулировать соотношение. А завтра измерения стали сильно отличаться ввиду вышесказанного, и опять одно
          добавляем, другое не добавляем.
        </p>

        <p>
          Пытаемся выровнять там что-то в аквариуме. А растения в это время перестают расти или ухудшаются, и всё тут.
        </p>

        <p>
          А если поставить вопрос с другой стороны:
        </p>

        <p>
          Почему мы в какой-то момент решили, что нужно тестировать нитрат и фосфат? Микроэлементы же вы не тестируете.
          А ответ простой: да просто потому, что такие тесты есть в магазине.
