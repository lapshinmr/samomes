          присутствовать на запуске. И только время нужно, чтобы все яды стали перерабатываться в относительно
          безопасный нитрат. Никакие подмены вам не могут помочь в этот момент. Подмены снижают уровень яда,
          но не избавляют полностью.
        </p>

