          или не пытайтесь сэкономить, используя очень дешевый грунт в аквариуме.
        </p>

        <h2 class="title font-weight-regular text-h4 my-10">
          Fe - тест на железо
        </h2>

        <p>
          Этот тест я отнесу к бесполезным или потенциально вредным. Его вредность заключается, как и у любых тестов, в
          создании ложного впечатления контроля, лишней суеты и ошибочных выводах.
        </p>

        <p>
          Вроде бы хороша идея измерять железо и таким образом отслеживать динамику поглощения микроэлементов, так как
          обычно их дозируют по железу. Но железо — крайне нестабильный элемент в воде. Его стабильность повышают
          хелаторами. Но настроить измерение железа тестом в зависимости от типа используемого хелатора — это очень
          сложная и ненужная задача.
        </p>

        <p>
          С другой стороны, если вы решите дозировать микроэлементы, ориентируясь на показания теста железа, и сделаете
          вывод, что микроэлементы уже израсходованы и нужно добавить еще, то эта дорога приведет вас к передозировке
          микроэлементов, если вы значительно отклонитесь от рекомендаций производителя. А это самое опасное для
          растений — отравить их тяжелыми металлами.
        </p>

