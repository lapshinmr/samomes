        <h2 class="title font-weight-regular text-h4 my-10">
          Kh и Gh - тесты на временную и постоянную жесткость
        </h2>

        <p>
          Вода имеет огромное значение в аквариуме с растениями. Этим часто пренебрегают или просто не осознают важности
          данного фактора. "Ну вода и вода, налил из крана и порядок" — нет, это совершенно не так. Вода содержит очень
          большое количество элементов, которые могут быть как полезны, так и вредны растениям и живности.
        </p>

        <p>
          Одним из первостепенных показателей является жесткость воды: постоянная и временная. Постоянная жесткость
          (Gh — General hardness) — это соли кальция и магния. Временная (Kh — Carbonate hardness)
          — количество гидрокарбонатов. Большинство аквариумных растений предпочитают мягкую воду в диапазоне 5-8 Gh.
        </p>

        <p>
          В случае с водопроводной водой, возможно, вам не обойтись без тестов на Gh и Kh. С осмосной водой
          (обессоленной, с Kh и Gh, равными нулю) всё понятно — мы сами создаём состав воды по своему желанию, исходя из
          расчётов и рекомендаций. Главное не ошибиться с реминерализацией. А с водопроводной водой, если нет знакомых
          аквариумистов, данных от водоканала или просто не у кого спросить, придётся приобрести тесты на Kh и Gh. Но
          они вам понадобятся всего лишь один раз, да и стоимость у них относительно невысокая.
        </p>

        <p>
          Жесткость воды обычно сильно не меняется. Возможны сезонные колебания, но они, как правило, не очень
          значительны и не должны сильно вредить растениям. Однако если вода существенно меняется в разные сезоны, то о
          стабильности в аквариуме не может быть и речи — тут уже без системы обратного осмоса не обойтись.
        </p>

        <p class="font-italic">
          Какой возможен вред от этих двух тестов?
        </p>

        <p>
          Главное, чего не следует делать, имея тесты на жесткость, — это раскачивать аквариум постоянным изменением
          состава воды. Например, если вы разбавляете водопроводную воду осмосом или используете осмос с
          реминерализатором и при этом регулярно проверяете жесткость, то можете столкнуться с тем, что тесты от недели
          к неделе показывают разные значения. В таком случае неопытный аквариумист пытается "выровнять" жесткость,
          досыпая или уменьшая количество реминерализатора, или изменяя пропорцию разбавления осмосом, если речь идёт о
          водопроводной воде.
        </p>

        <p>
          Но разница в показаниях может быть просто погрешностью измерения, ошибкой теста или неправильным проведением
          анализа. Тест вовсе может оказаться бракованным. Таким образом, вы создаёте колебания параметров воды в
          аквариуме и тем самым ухудшаете условия для растений и гидробионтов.
        </p>

        <p>
          В такой ситуации лучше немного ошибиться с подбором параметров (насыпать чуть меньше или больше
          реминерализатора), но при этом использовать одно и то же количество от подмены к подмене. Пусть будет не 8 Gh,
          а 9 или 7, но вы будете поддерживать стабильное значение на постоянной основе.
        </p>

        <p>
          Не могу сказать, что колебания жесткости в небольшом диапазоне — это серьёзная проблема, но всё же лучше этого
          избегать.
        </p>

        <h2 class="title font-weight-regular text-h4 my-10">
          Si - тест на кремний
        </h2>

        <p>
          Необходимость в тесте на кремний связана лишь с несколькими причинами: исключительно плохое качество воды,
          непригодный грунт или проблемные декорации в аквариуме. Это те факторы, с которыми нужно разобраться на старте
          аквариума — а именно, изучить свою воду и не добавлять в аквариум сомнительные материалы.
        </p>

        <p>
          Положительный момент в том, что случаи настолько плохой воды встречаются довольно редко. А грунт и декорации
          вы можете проконтролировать самостоятельно.
        </p>

        <p>
          Диатомовые водоросли — это обычное явление для нового аквариума. Зачастую они появляются между второй и
          третьей неделей, и аквариум сам справится с ними либо с помощью улиток и рыбок.
        </p>

        <p>
          Но если они появились заметно раньше — на первой неделе — и не уходят более месяца, тогда можно предпринимать
          дополнительные действия, вплоть до перезапуска аквариума и замены грунта или смены источника воды. Ошибаться
          нормально. Если вы хотите по-настоящему освоить это хобби, вам придётся запустить не менее десятка аквариумов.
          Я сам совсем недавно ошибся с камнями, и через два месяца пришлось перезапускать систему. Убрав проблемные
          камни, я сразу решил проблему с бурыми водорослями.
        </p>

