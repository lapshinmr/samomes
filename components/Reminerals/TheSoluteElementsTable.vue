<template>
  <div>
    <v-table>
      <thead>
        <tr>
          <th class="pl-0 text-center">
            {{ t('common.element') }}
          </th>
          <th
            v-for="([ion], index) in concentrationSorted"
            :key="ion"
            class="text-center"
            :class="{'pr-0': index === ionsTotal - 1}"
          >
            <div style="white-space: nowrap;">
              {{ ion }}, {{ remineral.isLiquid ? t('units.g/l') : t('units.g/1g') }}
            </div>
          </th>
        </tr>
      </thead>
      <tbody>
        <tr class="font-weight-medium">
          <td class="pl-0 text-center">
            {{ t('common.sum') }}
          </td>
          <template
            v-for="([ion, value], index) in concentrationSorted"
            :key="ion"
          >
            <td
              class="text-center"
              :class="{'pr-0': index === ionsTotal - 1}"
            >
              <template v-if="value">
                {{ format(value, 3) }}
              </template>
              <template v-else>
                â€”
              </template>
            </td>
          </template>
        </tr>
      </tbody>
    </v-table>
  </div>
</template>

<script lang="ts" setup>
const { t } = useI18n();

defineOptions({
  name: 'TheSoluteElementsTable',
});

const props = defineProps<{
  remineral: InstanceType<typeof RemineralRecipe>;
}>();

const concentrationSorted = computed(() => {
  return sortObject(props.remineral.concentration);
});

const ionsTotal = computed(() => {
  return Object.keys(props.remineral.concentration).length;
});
</script>

